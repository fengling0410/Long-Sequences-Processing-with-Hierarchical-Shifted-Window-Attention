        # W-MSA/SW-MSA
        attn_windows = self.attn(x_windows, shift_size=self.shift_size) # nW*B, window_size*window_size, C

        # reverse cyclic shift
        # print("seq_length:",self.seq_length)
        if self.shift_size > 0:
            if not self.fused_window_process:
                shifted_x = window_reverse(attn_windows, self.window_size, self.seq_length)  # B L C
